  @doc """
  Returns the list of reserved subdomains.

  ## Example

      iex> list_reserved_subdomains()
      ["www", "ftp", "smtp", ...]

  """
  def list_reserved_subdomains, do: @reserved_subdomains

  @doc """
  Checks if a subdomain is reserved.

  ## Examples

      iex> reserved?("www")
      true

      iex> reserved?("mycompany")
      false

  """
  def reserved?(subdomain) when is_binary(subdomain) do
    subdomain
    |> String.downcase()
    |> Kernel.in(@reserved_subdomains)
  end

  def reserved?(_subdomain), do: false
end
